function [d,d2] = der2(fun,x,fx)
% Обчислення першої і другої похідних
% функції однієї змінної
% Вхідні параметри
% fun - підпрограма обчислення функції
% x - початкова точка
% fx - значення функції у початковій точці
% Вихідні параметри
% d - перша похідна
% d2 - друга похідна
h = 1e-6;
u = x + h; fu = fun(u);
v = x - h; fv = fun(v);
d = (fu - fv)/(2*h);
d2 = (fu - 2*fx + fv)/h^2;